import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const root = process.cwd()
const productionPath = resolve(root, '.env.production')
const sourcePath = resolve(root, '.env')

const DEFAULTS = {
  VITE_CLOUD_FIRST: 'true',
  VITE_API_BASE_URL: 'https://tradeshow-passport-raffle.vercel.app',
}

function parseEnv(content) {
  const entries = new Map()

  for (const line of content.split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue

    const separatorIndex = trimmed.indexOf('=')
    if (separatorIndex === -1) continue

    const key = trimmed.slice(0, separatorIndex).trim()
    const value = trimmed.slice(separatorIndex + 1).trim()
    entries.set(key, value)
  }

  return entries
}

if (existsSync(productionPath)) {
  console.log('.env.production already exists — leaving it unchanged.')
  process.exit(0)
}

if (!existsSync(sourcePath)) {
  console.error(
    [
      'No .env.production or .env found.',
      'Copy .env.example to .env.production and fill in your VITE_* values.',
    ].join('\n'),
  )
  process.exit(1)
}

const sourceEntries = parseEnv(readFileSync(sourcePath, 'utf8'))
const viteEntries = [...sourceEntries.entries()].filter(([key]) => key.startsWith('VITE_'))

if (viteEntries.length === 0) {
  console.error('.env has no VITE_* variables to copy into .env.production.')
  process.exit(1)
}

const productionEntries = new Map(viteEntries)

for (const [key, value] of Object.entries(DEFAULTS)) {
  productionEntries.set(key, value)
}

const lines = [
  '# Generated by npm run env:mobile — client vars for Capacitor production builds',
  '# Vite loads this file during `vite build` (npm run build:mobile).',
  '# Vercel env vars do not apply to local native builds.',
  '',
  ...[...productionEntries.entries()].map(([key, value]) => `${key}=${value}`),
  '',
]

writeFileSync(productionPath, lines.join('\n'))
console.log(`Created ${productionPath} from .env (${viteEntries.length} VITE_* vars).`)
